		/// <summary>
        /// Creates active sink helper
        /// </summary>
		[EditorBrowsable(EditorBrowsableState.Never), Browsable(false)]
		public virtual void CreateEventBridge()
        {
			if(false == Factory.Settings.EnableEvents)
				return;
	
			if (null != _connectPoint)
				return;
	
            if (null == _activeSinkId)
				_activeSinkId = SinkHelper.GetConnectionPoint(this, ref _connectPoint, NetOffice.MSHTMLApi.Behind.EventContracts.HTMLXMLHttpRequestEvents_SinkHelper.Id);


			if(NetOffice.MSHTMLApi.Behind.EventContracts.HTMLXMLHttpRequestEvents_SinkHelper.Id.Equals(_activeSinkId, StringComparison.InvariantCultureIgnoreCase))
			{
				_hTMLXMLHttpRequestEvents_SinkHelper = new NetOffice.MSHTMLApi.Behind.EventContracts.HTMLXMLHttpRequestEvents_SinkHelper(this, _connectPoint);
				return;
			} 
        }

