		/// <summary>
        /// Creates active sink helper
        /// </summary>
		[EditorBrowsable(EditorBrowsableState.Never), Browsable(false)]
		public virtual void CreateEventBridge()
        {
			if(false == Factory.Settings.EnableEvents)
				return;
	
			if (null != _connectPoint)
				return;
	
            if (null == _activeSinkId)
				_activeSinkId = SinkHelper.GetConnectionPoint(this, ref _connectPoint, NetOffice.AccessApi.Behind.EventContracts._ObjectFrameEvents_SinkHelper.Id, NetOffice.AccessApi.Behind.EventContracts.DispObjectFrameEvents_SinkHelper.Id);


			if(NetOffice.AccessApi.Behind.EventContracts._ObjectFrameEvents_SinkHelper.Id.Equals(_activeSinkId, StringComparison.InvariantCultureIgnoreCase))
			{
				__ObjectFrameEvents_SinkHelper = new NetOffice.AccessApi.Behind.EventContracts._ObjectFrameEvents_SinkHelper(this, _connectPoint);
				return;
			}

			if(NetOffice.AccessApi.Behind.EventContracts.DispObjectFrameEvents_SinkHelper.Id.Equals(_activeSinkId, StringComparison.InvariantCultureIgnoreCase))
			{
				_dispObjectFrameEvents_SinkHelper = new NetOffice.AccessApi.Behind.EventContracts.DispObjectFrameEvents_SinkHelper(this, _connectPoint);
				return;
			} 
        }

