		/// <summary>
        /// Creates active sink helper
        /// </summary>
		[EditorBrowsable(EditorBrowsableState.Never), Browsable(false)]
		public void CreateEventBridge()
        {
			if(false == Factory.Settings.EnableEvents)
				return;
	
			if (null != _connectPoint)
				return;
	
            if (null == _activeSinkId)
				_activeSinkId = SinkHelper.GetConnectionPoint(this, ref _connectPoint, NetOffice.OutlookApi.Behind.EventContracts.OlkCategoryEvents_SinkHelper.Id);


			if(NetOffice.OutlookApi.Behind.EventContracts.OlkCategoryEvents_SinkHelper.Id.Equals(_activeSinkId, StringComparison.InvariantCultureIgnoreCase))
			{
				_olkCategoryEvents_SinkHelper = new NetOffice.OutlookApi.Behind.EventContracts.OlkCategoryEvents_SinkHelper(this, _connectPoint);
				return;
			} 
        }

